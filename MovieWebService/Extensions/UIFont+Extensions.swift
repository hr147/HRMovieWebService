import UIKit

extension UIFont {
    class var titleFont: UIFont {
        return UIFont.systemFont(ofSize: 18, weight: .medium)
    }
    
    class var valueFont: UIFont {
        return UIFont.systemFont(ofSize: 16.0)
    }
}
